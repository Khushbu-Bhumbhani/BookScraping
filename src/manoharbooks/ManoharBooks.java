    private static void startLinkScraping(String catUrl) {
        int offset = 40;
        String url = catUrl;
        String category = StringUtils.substringAfter(catUrl, "id=");
        boolean hasNextPage = false;
        do {
            try {
                System.out.println("->Get " + catUrl);
                Document doc = Jsoup.connect(catUrl)
                        .ignoreContentType(true)
                        .ignoreHttpErrors(true)
                        .userAgent("Mozilla/5.0 (Windows NT 10.0; WOW64; rv:64.0) Gecko/20100101 Firefox/64.0")
                        .get();
                if (doc.text().contains("HTTP Error 404.18") || doc.text().contains("The resource you are looking for has been removed")) {
                    System.out.println("404--return");
                    return;
                }
                for (Element a : doc.getElementsByTag("a")) {
                    if (a.attr("href").contains("BookDetails.asp?Id=")) {
                        String du = "https://www.manoharbooks.com/" + a.attr("href");
                        // System.out.println("" + du);
                        String insertQ = "INSERT INTO `books`.`manoharbooks_links`\n"
                                + "(\n"
                                + "`url`,\n"
                                + "`category`)\n"
                                + "VALUES\n"
                                + "("
                                + "'" + Utility.prepareString(du) + "',"
                                + "'" + Utility.prepareString(category) + "'"
                                + ")";
                        MyConnection.getConnection("books");
                        MyConnection.insertData(insertQ);
                    }
                }

                //pagination
                if (!doc.getElementsContainingOwnText("Next").isEmpty()) {
                    hasNextPage = true;
                    catUrl = url + "&offset=" + offset;
                    offset = offset + 40;

                } else {
                    hasNextPage = false;
                }
            } catch (IOException ex) {
                Logger.getLogger(ManoharBooks.class.getName()).log(Level.SEVERE, null, ex);
            }
        } while (hasNextPage);

    }
